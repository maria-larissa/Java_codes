public class TerceiraQuestao {
    public static void main(String[] args) {
        System.out.println("ACESSO PÚBLICO: permite que qualquer classe do projeto ");
        System.out.println("tenha acesso direto aos atributos e métodos daquela");
        System.out.println("classe.");

        System.out.println(" ");

        System.out.println("ACESSO PRIVADO: permite que somente a própria classe tenha");
        System.out.println("acesso aos atributos e métodos, ou seja, nenhuma outra classe");
        System.out.println("\"enxerga\" ou manipula os valores dos atributos.");

        System.out.println(" ");

        System.out.println("ACESSO PROTEGIDO: somente a própria classe e suas descendentes,");
        System.out.println("subclasses, mesmo em outros pacotes tem acesso aos  atributos");
        System.out.println("e métodos.");

        System.out.println(" ");

        System.out.println("ACESSO PADRÃO: assemelha-se ao acesso protegido, porém somente");
        System.out.println("as subclasses que estão no mesmo pacotem tem acesso aos");
        System.out.println("atributos e métodos.");
    }
}
